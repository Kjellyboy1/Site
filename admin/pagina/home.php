      <td border="0" align="left">De KLJ organiseert voornamelijk <em>ontspannende activiteiten</em>. Zo organiseren ze activiteiten<em>binnenshuis</em> zoals een nieuwjaarsreceptie. Daarbij worden verschillende spelletjes gespeeld en cadeautjes uitgedeeld. Verder organiseert de KLJ ook nog een gezelschapsavond en een streekbierenavond in het lokaal zelf.<br />
        <br />
        Maar ook <em>buitenshuis</em> zoals een lasershooting of dergelijke <em>stoere activiteiten</em>. Ook een uitstap naar de bioscoop, bezoek aan een pretpark of zwembad, enz kunnen aan bod komen.<br />
        <br />
        De KLJ organiseert ook <em>meerdaagse activiteiten</em>. Naast het jaarlijkse kamp gaat de KLJ ook geregeld op <em>daguitstap</em> (zoals een dagje Londen).<br />
        <br />
        Ook wordt er om de 2 jaar een <em>landjuweel</em> gehouden door een vlaamse-KLJ waarbij het vendellen, wimpelen en piramidebouwen centraal staan. Dit is een 2 dagen durend evenement.</td>
