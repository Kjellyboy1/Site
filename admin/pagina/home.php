      <td border="0" align="left">Het doel van de KLJ is vooral om zich te <em>amuseren</em>, om plezier te maken. De KLJ werd in 1927 opgericht en was oorspronkelijk bedoeld voor de plattelandsjongeren en is uit een vereniging voor landbouwers ontstaan. Tegenwoordig komen de meesten onder ons niet uit de landbouwsector.<br />
        <br />
        De doelgroep van de KLJ bestaat uit jongeren van <em>16 tot 20-25 jaar</em> (eigenlijk tot men getrouwd is), maar men kan eigenlijk al vanaf de leeftijd van <em>15 jaar</em> bij de vereniging aansluiten.</td>
