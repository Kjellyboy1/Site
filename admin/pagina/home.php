        <br />
        <br />
        Casa KLJ of Ten huize KLJ! Dit jaar helemaal in het teken van ons lokaal. Ons gezellig stulpje, fantastische partytent of machtige basis vanwaar we vertrekken voor onze wekelijkse activiteiten. <img src="http://www.kljgistel.be/_images/smilies/smile.gif" border="0" alt="smile" /></td>
